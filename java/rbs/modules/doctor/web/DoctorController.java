			if(keyFieldCode.equals("loginId")) {
				param.put("KEY_FIELD", "LOGINID");
			} else {
				param.put("KEY_FIELD", "HNAME");
			}
			param.put("KEY", "%" + keyCode + "%");
			model.addAttribute("isSearchList", new Boolean(true));
